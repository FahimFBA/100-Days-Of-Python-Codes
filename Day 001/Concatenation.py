# There are 3 ways to add space between the two strings

# 1st Way :

print("Hello " + "Fahim Bin Amin") # Output: Hello Fahim Bin Amin

# 2nd Way :

print("Hello" + " Fahim Bin Amin") # Output: Hello Fahim Bin Amin


# 3rd Way :

print("Hello" + " " + "Fahim Bin Amin") # Output: Hello Fahim Bin Amin